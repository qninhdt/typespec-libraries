/**
 * GormStruct -JSX component for rendering a complete GORM model Go file.
 *
 * Top-level orchestrator that composes GormField, GormRelationField, GormEnum
 * into a complete Go source file wrapped in <SourceFile>.
 *
 * Uses a generate-string approach: builds the full file content as a string,
 * then wraps it in <SourceFile> for Alloy.js to write to disk.
 */

import { SourceFile } from "@alloy-js/core";
import type { Children } from "@alloy-js/core/jsx-runtime";
import type { Model, Program } from "@typespec/compiler";
import {
  getCompositeIndexes,
  getCompositeUniques,
  getDoc,
  classifyProperties,
  camelToPascal,
  camelToSnake,
  resolveDbType,
} from "@qninhdt/typespec-orm";
import { GO_TYPE_MAP, buildCompositeMap } from "./GormConstants.js";
import { generateFieldLine, generateIgnoredFieldLine } from "./GormField.jsx";
import { generateAutoFkFieldLine, generateRelationFieldLine } from "./GormRelationField.jsx";

export interface GormModelFileProps {
  readonly program: Program;
  readonly model: Model;
  readonly tableName: string;
  readonly packageName: string;
}

/**
 * JSX component: renders a complete Go source file for a GORM model.
 */
export function GormModelFile(props: GormModelFileProps): Children {
  const { program, model, tableName, packageName } = props;
  const fileName = camelToSnake(model.name) + ".go";

  const imports = new Set<string>();

  // Build composite constraint map
  const compIdxs = getCompositeIndexes(program, model);
  const compUnqs = getCompositeUniques(program, model);
  const compositeMap = buildCompositeMap(compIdxs, compUnqs);

  // Classify properties
  const {
    enumTypes,
    ignored,
    relations,
    fields: regularProps,
  } = classifyProperties(program, model);

  // Generate field lines (populates imports as a side effect)
  const fieldLines: string[] = [];
  const relationFieldLines: string[] = [];

  // Ignored fields → gorm:"-"
  for (const { prop, enumInfo } of ignored) {
    const dbType = resolveDbType(prop.type);
    const mapping = dbType ? GO_TYPE_MAP[dbType] : undefined;
    let goType = mapping?.goType ?? "interface{}";
    if (enumInfo) goType = camelToPascal(enumInfo.enumType.name);
    if (mapping?.imports) {
      for (const imp of mapping.imports) imports.add(imp);
    }
    fieldLines.push(generateIgnoredFieldLine(program, prop, imports, goType));
  }

  // Relation FK auto-injection + navigation fields
  for (const { prop, resolved } of relations) {
    if (resolved.autoInjectFk) {
      fieldLines.push(generateAutoFkFieldLine(program, prop, resolved, imports, compositeMap));
    }
    relationFieldLines.push(generateRelationFieldLine(program, prop, resolved));
  }

  // Regular DB-mapped fields
  for (const { prop } of regularProps) {
    fieldLines.push(generateFieldLine(program, prop, compositeMap, imports));
  }

  // Build enum block
  const enumLines: string[] = [];
  for (const [enumName, members] of enumTypes) {
    const goTypeName = camelToPascal(enumName);
    enumLines.push(`// ${goTypeName} represents the ${camelToSnake(enumName)} enum.`);
    enumLines.push(`type ${goTypeName} string`);
    enumLines.push("");
    enumLines.push("const (");
    for (const m of members) {
      const constName = `${goTypeName}${camelToPascal(m.name)}`;
      enumLines.push(`\t${constName} ${goTypeName} = "${m.value}"`);
    }
    enumLines.push(")");
    enumLines.push("");
  }

  // Build import block
  const importBlock = buildImportBlock(imports);

  // Assembly
  const structName = model.name;
  const modelDoc = getDoc(program, model);
  const docLine = modelDoc
    ? `// ${structName} ${modelDoc}`
    : `// ${structName} represents the ${tableName} table.`;

  const lines: string[] = [];
  lines.push("// Code generated by @qninhdt/typespec-gorm. DO NOT EDIT.");
  lines.push("// Source: https://github.com/qninhdt/typespec-libraries");
  lines.push("");
  lines.push(`package ${packageName}`);
  lines.push("");
  if (importBlock) lines.push(importBlock);
  if (enumLines.length > 0) lines.push(enumLines.join("\n"));
  lines.push(docLine);
  lines.push(`type ${structName} struct {`);
  lines.push(fieldLines.join(""));
  if (relationFieldLines.length > 0) {
    lines.push("\t// ─── Relationships ─────────────────────");
    lines.push(relationFieldLines.join(""));
  }
  lines.push("}");
  lines.push("");
  lines.push(`// TableName returns the table name for ${structName}.`);
  lines.push(`func (${structName}) TableName() string {`);
  lines.push(`\treturn "${tableName}"`);
  lines.push("}");
  lines.push("");

  const content = lines.join("\n");

  return (
    <SourceFile path={fileName} filetype="go" printWidth={9999}>
      {content}
    </SourceFile>
  );
}

function buildImportBlock(imports: Set<string>): string {
  const sorted = [...imports].sort();
  if (sorted.length === 0) return "";
  const stdImports = sorted.filter((i) => !i.includes("."));
  const extImports = sorted.filter((i) => i.includes("."));
  const parts: string[] = [];
  parts.push("import (");
  for (const imp of stdImports) parts.push(`\t"${imp}"`);
  if (stdImports.length > 0 && extImports.length > 0) parts.push("");
  for (const imp of extImports) parts.push(`\t"${imp}"`);
  parts.push(")");
  return parts.join("\n") + "\n";
}
