/**
 * Convert an array of column-level arguments (e.g. `nullable=False`, `server_default="0"`) into
 * a Python dict literal suitable for `sa_column_kwargs={...}`.
 *
 * **Important**: this serializer only handles simple `key=value` entries. It must NOT be called
 * when `columnArgs` contains callable expressions like `ForeignKey(...)` - those must go through
 * the `sa_column=Column(...)` path.
 */
function serializeColumnKwargs(columnArgs: string[]): string {
  const pairs = columnArgs.map((a) => {
    const eqIdx = a.indexOf("=");
    if (eqIdx === -1) return `"${a}": True`;
    const key = a.substring(0, eqIdx);
    const val = a.substring(eqIdx + 1);
    return `"${key}": ${val}`;
  });
  return `{${pairs.join(", ")}}`;
}

/**
 * Move Field-level `index=True`, `unique=True`, and `foreign_key="ref"` into Column-level args
 * when generating `sa_column=Column(...)`.  Returns a new `fieldArgs` with those entries removed.
 *
 * This is necessary because SQLModel ignores `Field(index=True, sa_column=Column(...))`
 * - the sa_column completely replaces the auto-generated column.
 */
function promoteFieldArgsToColumn(
  fieldArgs: string[],
  columnArgs: string[],
  saImports: Set<string>,
): string[] {
  const filtered: string[] = [];
  for (const a of fieldArgs) {
    if (a === "index=True") {
      columnArgs.push("index=True");
    } else if (a === "unique=True") {
      columnArgs.push("unique=True");
    } else if (a.startsWith("foreign_key=")) {
      // Convert Field-level foreign_key="ref" to Column-level ForeignKey("ref")
      const match = a.match(/^foreign_key="(.+)"$/);
      if (match) {
        saImports.add("sqlalchemy.ForeignKey");
        columnArgs.unshift(`ForeignKey("${match[1]}")`);
      }
    } else if (a.startsWith("nullable=") || a.startsWith("server_default=")) {
      // Already handled by columnArgs
      continue;
    } else {
      filtered.push(a);
    }
  }
  return filtered;
}

