/**
 * Group "module.Name" imports into { module → Set<Name> }.
 * Supports aliased imports: "sqlalchemy.dialects.postgresql.JSONB as JSONB"
 */
function groupImports(imports: Set<string>): Map<string, Set<string>> {
  const groups = new Map<string, Set<string>>();

  for (const imp of imports) {
    // Handle "some.module.Name as Alias" pattern
    const asMatch = imp.match(/^(.+)\.(\w+)\s+as\s+(\w+)$/);
    if (asMatch) {
      const [, mod, name, alias] = asMatch;
      const module = mod;
      if (!groups.has(module)) groups.set(module, new Set());
      groups.get(module)!.add(`${name} as ${alias}`);
      continue;
    }

    // Handle "module.Name" pattern
    const lastDot = imp.lastIndexOf(".");
    if (lastDot === -1) continue;

    const module = imp.substring(0, lastDot);
    const name = imp.substring(lastDot + 1);

    if (!groups.has(module)) groups.set(module, new Set());
    groups.get(module)!.add(name);
  }

  return groups;
}
