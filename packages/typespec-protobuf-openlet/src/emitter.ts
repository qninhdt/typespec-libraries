import { type EmitContext } from "@typespec/compiler";
import { resolvePath, joinPaths } from "@typespec/compiler";
import { reportDiagnostic, type ProtoEmitterOptions } from "./lib.js";
import { collectPackages, filterBuckets } from "./walker/collect-packages.js";
import { renderProtoMessage } from "./writer/proto-message.js";
import { renderProtoEnum } from "./writer/proto-enum.js";
import { renderProtoService } from "./writer/proto-service.js";
import { renderProtoFile } from "./writer/proto-file.js";
import { renderProtoComment } from "./writer/proto-comment.js";

const DEFAULT_HEADER = "Generated by @qninhdt/typespec-protobuf-openlet. DO NOT EDIT.";

/**
 * TypeSpec emitter entry. Walks every `@package` namespace and writes one
 * `.proto` file per package under `emitterOutputDir`, named after the
 * package's slash-separated path (e.g. `openlet/user/v1.proto`).
 */
export async function $onEmit(context: EmitContext<ProtoEmitterOptions>): Promise<void> {
  const { program, options } = context;
  if (program.compilerOptions.noEmit) return;
  const outputDir = options["output-dir"] ?? context.emitterOutputDir;

  const allBuckets = collectPackages(program);
  if (allBuckets.length === 0) {
    return;
  }

  const buckets = filterBuckets(allBuckets);

  const headerText = options.header ?? DEFAULT_HEADER;
  const headerLines = renderProtoComment(headerText);
  const fieldNameStyle = options["field-name-style"] ?? "snake_case";
  const emptyRequestRewrite = options["empty-request-rewrite"] !== false;
  const resolverOptions = {
    wellKnown: {
      timestamp: options["well-known"]?.timestamp,
      duration: options["well-known"]?.duration,
      date: options["well-known"]?.date,
      time: options["well-known"]?.time,
      decimal: options["well-known"]?.decimal,
    },
  };

  for (const bucket of buckets) {
    const imports: string[] = [];
    const bodyBlocks: string[][] = [];

    // Messages first (declaration order — typed Iterables preserve insertion).
    for (const model of bucket.messages) {
      const {
        lines,
        imports: msgImports,
        diagnostics,
      } = renderProtoMessage(program, model, {
        fieldNameStyle,
        resolverOptions,
      });
      imports.push(...msgImports);
      bodyBlocks.push(lines);
      for (const d of diagnostics) {
        reportDiagnostic(program, {
          code: d.code as any,
          target: d.target,

          format: d.args as any,
        });
      }
    }

    for (const e of bucket.enums) {
      const { lines, diagnostics } = renderProtoEnum(program, e);
      bodyBlocks.push(lines);
      for (const d of diagnostics) {
        reportDiagnostic(program, {
          code: d.code as any,
          target: d.target,

          format: d.args as any,
        });
      }
    }

    for (const iface of bucket.services) {
      const {
        lines,
        imports: svcImports,
        diagnostics,
      } = renderProtoService(program, iface, {
        emptyRequestRewrite,
      });
      imports.push(...svcImports);
      bodyBlocks.push(lines);
      for (const d of diagnostics) {
        reportDiagnostic(program, {
          code: d.code as any,
          target: d.target,

          format: d.args as any,
        });
      }
    }

    const content = renderProtoFile({
      header: headerLines,
      package: bucket.spec,
      imports,
      bodyBlocks,
    });

    const relativePath = packageNameToFilePath(bucket.spec.name);
    const targetPath = resolvePath(joinPaths(outputDir, relativePath));
    await program.host.writeFile(targetPath, content);
  }
}

/**
 * Convert a dotted proto package name to a slash-separated `.proto` file
 * path. `openlet.user.v1` → `openlet/user/v1.proto`.
 */
function packageNameToFilePath(packageName: string): string {
  return `${packageName.replace(/\./g, "/")}.proto`;
}
